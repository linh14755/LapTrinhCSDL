--Tao Bang TacGia
CREATE TABLE TacGia
(
	MATG INT IDENTITY PRIMARY KEY,
	TENTG NVARCHAR(50),
	DIACHI NVARCHAR(50),
	GHICHU NVARCHAR(100) default null
)
go
CREATE TABLE LinhVuc
(
	MALV INT IDENTITY PRIMARY KEY,
	TENLV NVARCHAR(50),
	GHICHU NVARCHAR(100) default null
)
GO
CREATE TABLE NhaXB
(
	MANXB INT IDENTITY PRIMARY KEY,
	TENNXB NVARCHAR(50),
	DIACHI NVARCHAR(100),
	SODT NVARCHAR(20),
	GHICHU NVARCHAR(100) default null
)
GO
CREATE TABLE Sach
(
	MASACH INT NOT NULL IDENTITY PRIMARY KEY,
	TENSACH NVARCHAR(50),
	MATG INT REFERENCES TacGia(MATG),
	MANXB INT REFERENCES NhaXB(MANXB),
	MALV INT REFERENCES LinhVuc(MALV),
	NAMXB DATE,
	SOLUONG INT DEFAULT 0,
	NGAYNHAP DATE,
	GHICHU NVARCHAR(100) default null
)
GO

CREATE TABLE DocGia
(
	MADG INT IDENTITY PRIMARY KEY,
	TAIKHOAN NVARCHAR(100),
	MATkHAU NVARCHAR(100),
	HOTEN NVARCHAR(100),
	NGAYSINH nvarchar(100),
	GIOITINH NVARCHAR(100),
	LOP NVARCHAR(100),
	DIACHI NVARCHAR(100),
	EMAIL NVARCHAR(100),
	GHICHU NVARCHAR(100) default null
)
GO

create TABLE TaiKhoan
(
	TAIKHOAN NVARCHAR(100)NOT NULL,
	MATKHAU NVARCHAR(100)NOT NULL,
	TEN NVARCHAR(100),
	DIACHI NVARCHAR(100),
	EMAIL NVARCHAR(100),
	SODT NVARCHAR(100)
)
GO
CREATE TABLE MuonSach
(
	MADG INT REFERENCES DocGia(MADG),
	MASACH INT REFERENCES Sach(MASACH),
	SOPHIEUMUON INT IDENTITY PRIMARY KEY,
	NGAYMUON DATE,
	NGAYTRA DATE,
	XACNHANTRA INT DEFAULT 0, -- 0 LA CHUA TRA --1 DA TRA
	GHICHU NVARCHAR(100) default null
)

GO
